    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        // TODO add your handling code here:
        
        try{
            //Carregar cliente no combobox
            IClienteDAO dao = new ClienteDAO();
            List<Cliente> lista = dao.listarTodos();
            
            DefaultComboBoxModel modelo = new DefaultComboBoxModel();
            
            for(Cliente cliente: lista){
                modelo.addElement(cliente.getNome());
            }
            cbCliente.setModel(modelo);
            
            //Carregar funcionario no combobox
            IMecanicoDAO dao2 = new MecanicoDAO();
            List<Mecanico> lista2 = dao2.listarTodos();
            
            DefaultComboBoxModel modelo2 = new DefaultComboBoxModel();
            
            for(Mecanico mecanico: lista2){
                modelo2.addElement(mecanico.getNome());
            }
            cbmecanico.setModel(modelo2);            
            
            //Carregar peças no combobox
            IPecasDAO dao3 = new PecasDAO();
            List<Pecas> lista3 = dao3.listarTodos();
            
            DefaultComboBoxModel modelo3 = new DefaultComboBoxModel();
            
            
            for(Pecas pecas: lista3){
                modelo3.addElement(pecas.getNome().trim()+ " | "+pecas.getPreco().trim());
                
            }
            cbPecas.setModel(modelo3);   
  
        }catch(Exception e){
            System.out.println(e.toString());
            JOptionPane.showMessageDialog(null, "Houve um erro ao carregar os dados");
        }
    }//GEN-LAST:event_formInternalFrameOpened

