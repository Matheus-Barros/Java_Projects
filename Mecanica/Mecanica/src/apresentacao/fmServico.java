    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // salvando
        int resposta = 0;
        int valor = 1;
        

        //Verifica se o automovel está vazio
        if(txtCarro.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Falha ao Salvar. Campo automovel vazio.");
            lbCarro.setForeground(Color.red);
            resposta = 1;
        }
        
        //Verifica se data enterga está vazio
        if(txtEntrega.getText().equals("  /  /    ")){
            JOptionPane.showMessageDialog(null, "Falha ao Salvar. Campo entrega vazio.");
            lbEntrega.setForeground(Color.red);
            resposta = 1;
        }        

        
        if(resposta==0){

            Servico servico = new Servico();
            servico.setCliente(cbCliente.getSelectedItem().toString());
            servico.setMecanico(cbmecanico.getSelectedItem().toString());
           
            if(chSituacao.isSelected()){
                servico.setSituacao("Pago");
            }else{
                servico.setSituacao("Não Pago");
            }
              
            servico.setAutomovel(txtCarro.getText());

            try{
            
                SimpleDateFormat data = new SimpleDateFormat("dd/MM/yyyy");
                Calendar cal = Calendar.getInstance();
                cal.setTime(data.parse(txtEntrega.getText()));
                servico.setEntrega(cal);
                
            }   catch(ParseException e){
            
                System.out.println(e);
            }            
            
            //salvando no BD
            servico.setPecas(txtPecas.getText());
            servico.setServico(txtServico.getText());
            lbCarro.setForeground(Color.black);            
            lbEntrega.setForeground(Color.black);

            IServicoDAO dao = new ServicoDAO();
            dao.adiciona(servico);
            JOptionPane.showMessageDialog(null, "Os dados foram gravados.");
            
        }
                   
  
    }//GEN-LAST:event_btSalvarActionPerformed

